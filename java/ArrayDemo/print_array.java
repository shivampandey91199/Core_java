package ArrayDemo;

public class print_array {
	
	    public static void main(String[] args) {
	        char[] messageArray = {'i', ' ', 'a', 'm', ' ', 'i', 'd', 'i', 'o', 't'};
	        
	        for (int i = 0; i < messageArray.length; i++) {
	            System.out.print(messageArray[i]);
	        }
	        System.out.println();
	    }
	


}
